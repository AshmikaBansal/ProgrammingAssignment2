## This takes input and will show you the matrix and its inverse.
## First of all amking a cache matrix
## Enter a square matrix only.

makeCacheMatrix <- function(x = matrix()) {
  
  i <- NULL
  set <- function(y) {
    x <<- y
    i <<- NULL
  }
  get <- function() x
  setinv  <- function(inv) i <<- inv
  getinv <- function() i
  list(set = set, get = get,
       setinv = setinv,
       getinv = getinv)
  

}


## Now checking whether our cache matrix contains the inverse of matrix or not.
## If not then it will calculate and set it to getinv function.
## After this it returns the inverse of the matrix
cacheSolve <- function(x, ...) {
  i <- x$getinv()
  if(!is.null(i)) {
    message("getting cached data")
    return(i)
  }
  data <- x$get()
  i <- solve(data, ...)
  x$setinv(i)
  i
}
